local AVOID_IDENTIFIERS = {
	["and"] = true,
	["break"] = true,
	["do"] = true,
	["else"] = true,
	["elseif"] = true,
	["end"] = true,
	["false"] = true,
	["for"] = true,
	["function"] = true,
	["if"] = true,
	["in"] = true,
	["local"] = true,
	["nil"] = true,
	["not"] = true,
	["or"] = true,
	["repeat"] = true,
	["return"] = true,
	["then"] = true,
	["true"] = true,
	["until"] = true,
	["while"] = true,
}

local function isValidLuauIdentifier(identifier: string): boolean
	return identifier:match("^[%a_][%w_]*$") and AVOID_IDENTIFIERS[identifier] == nil
end

return isValidLuauIdentifier
